package br.com.easycook.classesVO;

public class ReceitaVO {

	private int idReceita;
	private int porcao;
	private String modo_preparo;
	private int idEstilo_vida;
	private int idCategoria;
	
	
	public int getIdReceita() {
		return idReceita;
	}
	public void setIdReceita(int idReceita) {
		this.idReceita = idReceita;
	}
	public int getPorcao() {
		return porcao;
	}
	public void setPorcao(int porcao) {
		this.porcao = porcao;
	}
	public String getModo_preparo() {
		return modo_preparo;
	}
	public void setModo_preparo(String modo_preparo) {
		this.modo_preparo = modo_preparo;
	}
	public int getIdEstilo_vida() {
		return idEstilo_vida;
	}
	public void setIdEstilo_vida(int idEstilo_vida) {
		this.idEstilo_vida = idEstilo_vida;
	}
	public int getIdCategoria() {
		return idCategoria;
	}
	public void setIdCategoria(int idCategoria) {
		this.idCategoria = idCategoria;
	}
	
	
	
}
